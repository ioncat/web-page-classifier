def main():
    apply = "--apply" in sys.argv
    conn = sqlite3.connect(DB_PATH)

    # Текущие категории в БД
    rows = conn.execute(
        "SELECT DISTINCT category FROM urls WHERE category IS NOT NULL AND category != ''"
    ).fetchall()
    old_cats = [r[0] for r in rows]

    mapped_count = 0
    null_count = 0
    unmapped = []

    for cat in sorted(old_cats):
        new_cat = MAPPING.get(cat)
        cnt = conn.execute("SELECT COUNT(*) FROM urls WHERE category = ?", (cat,)).fetchone()[0]
        if new_cat:
            if new_cat != cat:
                print(f"  RENAME  {cat!r} ({cnt}) -> {new_cat!r}")
                if apply:
                    conn.execute("UPDATE urls SET category = ? WHERE category = ?", (new_cat, cat))
                mapped_count += cnt
            else:
                # уже совпадает с таксономией
                pass
        else:
            print(f"  -> NULL  {cat!r} ({cnt})")
            if apply:
                conn.execute("UPDATE urls SET category = NULL WHERE category = ?", (cat,))
            null_count += cnt
            unmapped.append((cat, cnt))

    if apply:
        # Пересоздаём таблицу tags из таксономии
        conn.execute("DELETE FROM tags")
        from config.taxonomy import TAXONOMY
        for name in TAXONOMY:
            conn.execute("INSERT OR IGNORE INTO tags (name) VALUES (?)", (name,))
        conn.commit()
        print(f"\nПрименено. Переименовано: {mapped_count}, обнулено: {null_count}")
        print(f"Таблица tags пересоздана: {len(TAXONOMY)} категорий")
    else:
        print(f"\nDRY RUN. Будет переименовано: {mapped_count}, обнулено: {null_count}")
        if unmapped:
            print(f"Без маппинга (→ NULL): {len(unmapped)} категорий, {null_count} URL")
        print("Запусти с --apply для применения.")

    conn.close()


if __name__ == "__main__":
    main()
